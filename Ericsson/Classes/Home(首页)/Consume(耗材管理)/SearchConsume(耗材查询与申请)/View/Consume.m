#import "Consume.h"

@implementation Consume
-(id)copyWithZone:(NSZone *)zone
{
    Consume *consume = [[Consume allocWithZone:zone]init];
    consume.consumableName=_consumableName;
    consume.consumableModel=_consumableModel;
    consume.consumableId=_consumableId;
    consume.applyNumber=_applyNumber;
    
   
    return consume;
}

@end
